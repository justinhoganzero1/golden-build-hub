const buildPackageId = (url: string, appName: string) => {
  const host = url.replace(/https?:\/\//, "").split("/")[0].replace(/[^a-z0-9.]/gi, "").toLowerCase();
  const parts = host.split(".").filter(Boolean);
  const domain = parts.length >= 2 ? parts[parts.length - 2] : "mysite";
  const name = appName.replace(/\s+/g, "").replace(/[^a-z0-9]/gi, "").toLowerCase() || "myapp";
  return `com.${domain}.${name}`;
};

